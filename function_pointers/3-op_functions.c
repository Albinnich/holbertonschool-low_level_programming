#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - function to sum a and b
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */

int op_add(int a, int b);

{
	return(a + b);
}

/**
 * op_sub - function to subtract a and b
 * @a: first integer
 * @b: second integer
 * Return: result of a and b
 */

int op_sub(int a, int b);

{
	return(a - b);
}

/**
 * op_mul - function to multiply a and b
 * @a: first integer
 * @b: second integer
 * Return: product of a and b
 */

int op_mul(int a, int b);

{
	return(a * b);
}

/**
 * op_div - function to divide a and b
 * @a: first integer
 * @b: second integer
 * Return: division of a and b
 */

int op_div(int a, int b);

{
	return(a / b);;
}

/**
 * op_mod - function to find remainder of a and b
 * @a: first integer
 * @b: second integer
 * Return: remainder of a and b
 */

int op_mod(int a, int b);

{
	return(a % b);
}
